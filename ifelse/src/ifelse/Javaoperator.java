package ifelse;

public class Javaoperator {

	public static void main(String[] args) {
		
   int a=42;
   int b=10;
   
   System.out.println(a%b);
   System.out.println(b>a);
   System.out.println(a!=b);
   System.out.println(a>b&&b>a);
   System.out.println(a>b||b>a);
   
   b+=3;
   System.out.println(b);
   
   a++;
   System.out.println(a);
   
   
		
		
	}

}
